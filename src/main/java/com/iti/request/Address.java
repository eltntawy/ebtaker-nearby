package com.iti.request;

public class Address {
	
	String name;
	String vicinity;
	
	public void setName(String name){
		this.name = name;
	}
	
	public void setVicinity(String vicinity){
		this.vicinity = vicinity;
	}
	
	public String getName() {
		return name;
	}
	
	public String getVicinity(){
		return vicinity;
	}

}
